"""
Quick analysis for the human-readable timelines generated by
`ExplainResults/3-export_timelines_txt.py`.

It parses headers like:
    === 用户 10 [DOWN] (min_diff 64.0, UNISAR 13, TrustSAR 77; max_diff 64.0, UNISAR 13, TrustSAR 77) ===
and event lines like:
    [2024-11-27 03:07:02] (search) 大金空调介绍 | item: 拒绝选择困难，大金空调选购指南来咯❗️
    [2024-11-26 20:36:15] (rec) 男人爱惨了你的10种表现

Summary printed:
- user / event counts
- UP / DOWN / both user counts
- search vs rec event counts
- basic per-user event statistics (mean/median)
"""

from pathlib import Path
import re
from statistics import mean, median
import pandas as pd

ROOT = Path(__file__).resolve().parent

# Prefer latest file; fall back to dated version if needed.
for name in ["extreme_timelines_global.txt", "extreme_timelines_global0202.txt"]:
    FILE = ROOT / name
    if FILE.exists():
        break
else:
    raise FileNotFoundError("extreme_timelines_global*.txt not found in New_results/")

HEADER_RE = re.compile(
    r"^=== 用户 (\d+) \[([^\]]*)\] "
    r"\(min_diff ([\-0-9.]+), UNISAR (\d+), TrustSAR (\d+); "
    r"max_diff ([\-0-9.]+), UNISAR (\d+), TrustSAR (\d+)\) ===$"
)
EVENT_RE = re.compile(r"^\[(.+?)\]\s+\((search|rec)\)\s+(.*)")


def parse_file(path: Path):
    users = []
    events = []
    current_uid = None

    with path.open("r", encoding="utf-8") as f:
        for line in f:
            line = line.rstrip("\n")
            if not line:
                continue

            m = HEADER_RE.match(line)
            if m:
                uid = int(m.group(1))
                tags = set(m.group(2).split("/")) if m.group(2) else set()
                users.append(
                    {
                        "uid": uid,
                        "tags": tags,
                        "min_diff": float(m.group(3)),
                        "min_unisar": int(m.group(4)),
                        "min_trust": int(m.group(5)),
                        "max_diff": float(m.group(6)),
                        "max_unisar": int(m.group(7)),
                        "max_trust": int(m.group(8)),
                    }
                )
                current_uid = uid
                continue

            m = EVENT_RE.match(line)
            if m and current_uid is not None:
                ts, ev_type, payload = m.groups()
                query_text = ""
                item_caption = payload
                if ev_type == "search" and " | item: " in payload:
                    query_text, item_caption = payload.split(" | item: ", 1)
                events.append(
                    {
                        "uid": current_uid,
                        "timestamp": ts,
                        "event": ev_type,
                        "query_text": query_text,
                        "item_caption": item_caption,
                    }
                )

    return pd.DataFrame(users), pd.DataFrame(events)


def main():
    users_df, events_df = parse_file(FILE)

    total_users = len(users_df)
    total_events = len(events_df)
    print(f"File: {FILE.name}")
    print(f"Users: {total_users}")
    print(f"Events: {total_events}")
    print(f"Avg events per user: {round(total_events / total_users, 2) if total_users else 0}")

    tag_counts = {
        "UP": sum("UP" in t for t in users_df["tags"]),
        "DOWN": sum("DOWN" in t for t in users_df["tags"]),
        "UP&DOWN": sum(t == {"UP", "DOWN"} for t in users_df["tags"]),
    }
    print("\nUser tags:", tag_counts)

    print("\nEvents by type:")
    print(events_df["event"].value_counts())

    events_per_user = events_df.groupby("uid").size()
    print("\nEvents per user:")
    print(
        {
            "mean": round(mean(events_per_user), 2),
            "median": median(events_per_user),
            "min": int(events_per_user.min()),
            "max": int(events_per_user.max()),
        }
    )

    # Simple keyword tally on search queries
    keywords = ["文案", "穿搭", "教程", "使用", "头像", "壁纸", "攻略", "推荐", "测评", "直播", "网速", "wifi"]
    def extract_keywords(text):
        found = []
        for kw in keywords:
            if kw in text:
                found.append(kw)
        return found

    # 按用户标签拆分关键词热度
    user_tag_map = {row.uid: row.tags for row in users_df.itertuples()}
    events_df["group"] = events_df["uid"].apply(
        lambda uid: "BOTH"
        if {"UP", "DOWN"} <= user_tag_map.get(uid, set())
        else ("UP" if "UP" in user_tag_map.get(uid, set()) else ("DOWN" if "DOWN" in user_tag_map.get(uid, set()) else "OTHER"))
    )

    def top_keywords_for(group):
        sub = events_df[(events_df["event"] == "search") & (events_df["group"] == group)]
        return (
            sub["query_text"]
            .dropna()
            .apply(extract_keywords)
            .explode()
            .value_counts()
            .head(10)
        )

    print("\nTop search keywords by group (simple contains):")
    for g in ["UP", "DOWN", "BOTH"]:
        print(f"\n{g}:")
        print(top_keywords_for(g))


if __name__ == "__main__":
    main()
